public class AsciiEx {

    public static void main(String[] args) {

        char ch = 'a';
        int ascii = ch;
        int cAscii = (int) ch;

        System.out.println("The ASCII value of " + ch + " is: " + ascii);
        System.out.println("The ASCII value of " + ch + " is: " + cAscii);
    }
}
